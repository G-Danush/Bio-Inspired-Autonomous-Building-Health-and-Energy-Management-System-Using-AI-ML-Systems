def rule_based_detection(data):
    score = 0
    alerts = []
    risk_levels = []
    # =====================================================
    # TEMPERATURE RISK
    # =====================================================
    if data["temp"] > 31:
        temp_risk = (
            data["temp"] - 31
        ) / 3
        score += temp_risk
        alerts.append(
            "Overheating Detected"
        )
        risk_levels.append(
            "HIGH"
        )
    # =====================================================
    # HUMIDITY RISK
    # =====================================================
    if data["humidity"] > 72:
        humidity_risk = (
            data["humidity"] - 72
        ) / 6
        score += humidity_risk
        alerts.append(
            "Humidity Spike"
        )
        risk_levels.append(
            "MEDIUM"
        )
    # =====================================================
    # VIBRATION RISK
    # =====================================================
    if data["vibration"] > 3.5:
        vibration_risk = (
            data["vibration"] - 3.5
        )
        score += vibration_risk
        alerts.append(
            "Abnormal Structural Vibration"
        )
        risk_levels.append(
            "HIGH"
        )
    # =====================================================
    # STRAIN RISK
    # =====================================================
    if data["strain"] > 6.5:
        strain_risk = (
            data["strain"] - 6.5
        )
        score += strain_risk
        alerts.append(
            "Structural Instability"
        )
        risk_levels.append(
            "CRITICAL"
        )
    # =====================================================
    # ENERGY RISK
    # =====================================================
    if (
        data["energy"] > 380
        and data["temp"] > 30
    ):
        energy_risk = (
            data["energy"] - 380
        ) / 35
        score += energy_risk
        alerts.append(
            "Energy Efficiency Problem"
        )
        risk_levels.append(
            "HIGH"
        )
    # =====================================================
    # ENVIRONMENTAL FUSION
    # =====================================================
    if (
        data["temp"] > 31
        and data["humidity"] > 72
    ):
        score += 4
        alerts.append(
            "Environmental Multi-Risk"
        )
        risk_levels.append(
            "CRITICAL"
        )
    # =====================================================
    # STRUCTURAL FUSION
    # =====================================================
    if (
        data["vibration"] > 3.5
        and data["strain"] > 6.5
    ):
        score += 5
        alerts.append(
            "Critical Structural Risk"
        )
        risk_levels.append(
            "SEVERE"
        )
    return score, alerts, risk_levels


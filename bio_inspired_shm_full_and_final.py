def generate_normal_data(t):
    # -----------------------------------------------------
    # OCCUPANCY
    # -----------------------------------------------------
    occupancy = np.random.randint(20, 100)
    # -----------------------------------------------------
    # TEMPERATURE
    # OCCUPANCY + ENVIRONMENTAL EFFECT
    # -----------------------------------------------------
    temp = (
        22
        + 0.03 * occupancy
        + 3 * np.sin(t / 20)
        + np.random.normal(0, 0.7)
    )
    # -----------------------------------------------------
    # HUMIDITY
    # CORRELATED WITH OCCUPANCY
    # -----------------------------------------------------
    humidity = (
        50
        + 0.02 * occupancy
        + 8 * np.sin(t / 25)
        + np.random.normal(0, 1.5)
    )
    # -----------------------------------------------------
    # VIBRATION
    # OCCUPANCY-DEPENDENT
    # -----------------------------------------------------
    vibration = (
        1.5
        + 0.01 * occupancy
        + 0.5 * np.sin(t / 15)
        + np.random.normal(0, 0.12)
    )
    # -----------------------------------------------------
    # STRAIN
    # CORRELATED WITH VIBRATION
    # -----------------------------------------------------
    strain = (
        4
        + 0.5 * vibration
        + np.random.normal(0, 0.2)
    )
    # -----------------------------------------------------
    # ENERGY CONSUMPTION
    # OCCUPANCY + HVAC LOAD
    # -----------------------------------------------------
    energy = (
        180
        + occupancy * 2
        + temp * 4
        + np.random.normal(0, 4)
    )
    return {
        "temp": temp,
        "humidity": humidity,
        "vibration": vibration,
        "strain": strain,
        "occupancy": occupancy,
        "energy": energy
    }

# =========================================================
# ADVANCED PREPROCESSING
# =========================================================
def preprocess(data):
    # -----------------------------------------------------
    # CLIP UNREALISTIC VALUES
    # -----------------------------------------------------
    data["temp"] = np.clip(
        data["temp"],
        10,
        50
    )
    data["humidity"] = np.clip(
        data["humidity"],
        20,
        100
    )
    # =====================================================
    # BASIC FEATURE ENGINEERING
    # =====================================================
    data["temp_norm"] = (
        data["temp"] / 50
    )
    data["vibration_energy"] = (
        data["vibration"] *
        data["energy"]
    )
    data["temp_humidity"] = (
        data["temp"] *
        data["humidity"]
    )
    data["strain_vibration"] = (
        data["strain"] *
        data["vibration"]
    )
    # =====================================================
    # ADVANCED RELATIONSHIP FEATURES
    # =====================================================
    # -----------------------------------------------------
    # HVAC EFFICIENCY
    # -----------------------------------------------------
    data["temp_energy_ratio"] = (
        data["temp"] /
        (data["energy"] + 1e-6)
    )
    # -----------------------------------------------------
    # STRUCTURAL CONSISTENCY
    # -----------------------------------------------------
    data["vibration_strain_ratio"] = (
        data["vibration"] /
        (data["strain"] + 1e-6)
    )
    # -----------------------------------------------------
    # OCCUPANCY ENERGY BEHAVIOR
    # -----------------------------------------------------
    data["occupancy_energy_ratio"] = (
        data["occupancy"] /
        (data["energy"] + 1e-6)
    )
    # -----------------------------------------------------
    # ENVIRONMENTAL LOAD
    # -----------------------------------------------------
    data["environmental_load"] = (
        data["temp"] *
        data["humidity"]
    ) / 100
    return data

